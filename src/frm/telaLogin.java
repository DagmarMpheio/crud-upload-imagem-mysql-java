    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        telaHome home = new telaHome();
        try {
            Usuario u = new Usuario();
            UsuarioDao dao = new UsuarioDao();

            u.setLogin(txtLogin.getText());
            u.setSenha(new String(txtSenha.getPassword()));

            if (dao.verificarLogin(u)) {
                JOptionPane.showMessageDialog(null, "Login Efectuado Com Sucesso!");
                this.dispose();
                home.setVisible(true);
                home.lblUser.setText("Usu√°rio Activo: " + txtLogin.getText());

            } else {
                JOptionPane.showMessageDialog(null, "Login ou Senha Incorrecta!");
                txtLogin.setText("");
                txtSenha.setText("");
            }
        } catch (SQLException ex) {
            Logger.getLogger(telaLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed
